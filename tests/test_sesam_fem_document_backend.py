def test_reader_accepts_fixed_width_numeric_fields() -> None:
    path = _work_dir() / "fixed_width.FEM"
    gnode = "GNODE   " + "".join(
        [
            f"{1:16d}",
            f"{0:16d}",
            f"{1.25:16.8E}",
            f"{-2.5:16.8E}",
            f"{0.0:16.8E}",
        ]
    )
    path.write_text("\n".join(["IDENT          1", gnode, "IEND", ""]), encoding="ascii")

    records = read_raw_records(path)
    assert records[1].numeric_fields == pytest.approx((1.0, 0.0, 1.25, -2.5, 0.0))

    document = read_sesam_fem_document(path)
    assert document.nodes[1].coordinates == pytest.approx((1.25, -2.5, 0.0))


