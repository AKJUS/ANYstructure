def test_writer_roundtrip_and_no_overwrite_guard() -> None:
    work_dir = _work_dir()
    input_path = work_dir / "roundtrip_in.FEM"
    output_path = work_dir / "roundtrip_out.FEM"
    input_path.write_text(_mixed_shell_fem(), encoding="ascii")
    if output_path.exists():
        output_path.unlink()

    document = read_sesam_fem_document(input_path)
    report = write_sesam_fem_document(document, output_path)
    reread = read_sesam_fem_document(output_path)

    assert report.records_written >= len(document.raw_records)
    assert reread.record_counts["IEND"] == 1
    assert reread.elements[200].type_code == 24
    with pytest.raises(SesamFemError) as exc_info:
        write_sesam_fem_document(document, output_path)
    assert exc_info.value.code == "FEM201"


def test_rejects_sin_sif_binary_and_missing_iend() -> None:
    work_dir = _work_dir()
    sin_path = work_dir / "case.SIN"
    sif_path = work_dir / "case.SIF"
    binary_path = work_dir / "binary.FEM"
    missing_iend_path = work_dir / "missing_iend.FEM"
    sin_path.write_text("IDENT          1\nIEND\n", encoding="ascii")
    sif_path.write_text("IDENT          1\nIEND\n", encoding="ascii")
    binary_path.write_bytes(b"\x00\x01\x02not formatted fem")
    missing_iend_path.write_text("IDENT          1\nGNODE           1 0 0 0 0\n", encoding="ascii")

    for path in (sin_path, binary_path):
        with pytest.raises(SesamFemError) as exc_info:
            read_sesam_fem_document(path)
        assert exc_info.value.code == "FEM001"

    with pytest.raises(SesamFemError) as exc_info:
        read_sesam_fem_document(missing_iend_path)
    assert exc_info.value.code == "FEM003"

    document = read_sesam_fem_document(missing_iend_path, strict=False)
    assert any(item.code == "FEM003" for item in document.diagnostics)


